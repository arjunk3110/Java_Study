package oops_Polymorphism;

class A
{
	void show()
	{
		System.out.println("Showing class A ");
	}

}
class B extends A

{
	void show()
	{
		System.out.println("Showing class B ");
	}	
}
	



public class MethodOverRiding 

{
public static void main(String[] args)
	
	{
		MethodOverRiding obj = new MethodOverRiding();

	}

}
